                        <p style="text-align: left; line-height: 200%; text-indent: 21pt">&nbsp;</p>
                        <p style="text-align: left; line-height: 200%; text-indent: 21pt">MINISO名創優品是日本快时尚设计师品牌，总部位于日本东京，由日本设计师三宅顺也和中国青年企业家叶国富先生共同创办，三宅顺也先生同时兼任首席设计师。MINISO名創優品，是&ldquo;时尚休闲生活优品消费&rdquo;领域的倡导者。</p>
                        <p style="text-align: left; line-height: 200%; text-indent: 21pt">MINISO名創優品奉行&ldquo;简约、自然、富质感&rdquo;的生活哲学和&ldquo;自然、自在，回归本质&rdquo;的品牌主张，以每七天上一批炫酷新款、走低价新锐路线、定位快时尚休闲百货连锁的品牌核心优势赢得消费者们的亲睐和热捧，在时尚消费前沿市场先后刮起&ldquo;生活优品消费&rdquo;之风。2013年由赛曼基金携手中国广州财团引进，由广东葆扬投资管理有限公司运营，MINISO名創優品开始进驻中国，全面布局在华时尚休闲百货市场。目前，MINISO名創優品在全球正式营业和签约在建的店铺数已达数千家，平均每月开店80-100家。</p>
                        <p style="text-align: left; line-height: 200%; text-indent: 21pt">MINISO名創優品一直倡导优质生活理念，并秉承&ldquo;尊重消费者&rdquo;的品牌精神，致力于为消费者提供真正&quot;优质、创意、低价&quot;的产品。MINISO名創優品的产品简约自然，品质优良且紧跟全球时尚潮流，产品价格大部分在10-29元间，深受18到35岁的小资、白领等主流消费人群的喜爱。</p>
                        <p style="text-align: left; line-height: 200%; text-indent: 21pt">MINISO名創優品同时开创了新型的时尚休闲生活方式集合店，与餐饮、快时尚服饰、娱乐共同成为百货公司和购物中心的主力店铺，并在实现优质生活百货的&quot;快时尚&quot;消费主张的同时，着力于营造全新的时尚休闲购物氛围，让顾客在消费中体验快乐、时尚、健康的生活方式。&nbsp;</p>
                        <p style="text-align: left; line-height: 200%; text-indent: 21pt">MINISO名創優品始终将优化商品结构和商品管理列为首要条件，坚持从世界各地选取合适的优质素材，其中超过80％的产品设计源于日本、韩国、瑞典、丹麦、新加坡、马来西亚及中国等地。在坚持优质产品和时尚创意的同时，MINISO名創優品也极其注重顾客购物体验，致力于优质服务文化的打造。MINISO名創優品在全球的店面大多选取繁华人气商圈，全力打造一个贴近民生并具有时尚调性的休闲百货品牌，为消费者带来更多更好的购物体验，将MINISO名創優品休闲、时尚、超值、优质、创意的品牌调性在体验层面完美体现。</p>
